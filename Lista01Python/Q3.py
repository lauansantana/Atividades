'''Implemente  um  programa  capaz  de  solicitar  ao  usuário  suas  notas  da  AV1  e  da AV2. O programa deve imprimir qual é a sua média.'''


av1 = int(input('Informe a sua nota da AV1: '))
av2 = int(input('Informe a sua nota da AV2: '))
media = (av1+av2)/2
print('A sua média é: {}'.format(media))
